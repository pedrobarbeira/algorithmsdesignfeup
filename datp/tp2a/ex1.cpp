#include "exercises.h"

Labyrinth::Labyrinth(int values[10][10]) {
    for (int i = 0; i < 10; i++)
        for (int j = 0; j < 10; j++)
            labyrinth[i][j] = values[i][j];
}

#include <iostream>

void  Labyrinth::print() const {
    for (int i = 0; i < 10; i++) {
        for (int j = 0; j < 10; j++) {
            std::cout << labyrinth[i][j] << " ";
        }
        std::cout << std::endl;
    }
}

bool Labyrinth::findGoal(int x, int y) {
    visited[x][y] = true;

    if (labyrinth[x][y] == 2) return true;
    else {
        int a, b;

        std::pair<int, int> choices[4];

        std::pair<int, int> up(x - 1, y);
        std::pair<int, int> down(x + 1, y);
        std::pair<int, int> left(x, y - 1);
        std::pair<int, int> right(x, y + 1);

        choices[0] = up;
        choices[1] = down;
        choices[2] = left;
        choices[3] = right;

        for (int i = 0; i < 4; i++) {
            a = choices[i].first;
            b = choices[i].second;
            if(!visited[a][b] && labyrinth[a][b] != 0) {
                if (findGoal(a, b)) {
                    std::cout << "Congratulations, you've won!\n";
                    return true;
                }
            }
        }
    }
}

void Labyrinth::initializeVisited() {
    for(int i = 0; i < 10; i++)
        for(int j = 0; j < 10; j++)
            visited[i][j] = false;
}

